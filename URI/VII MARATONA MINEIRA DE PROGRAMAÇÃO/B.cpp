int sizedfs(int u, int p) {
	size[u] = 1; fson[u] = -1; par[u] = p;
	int msz = 0;
	for(int i=0; i<(int)adjList[u].size(); i++) {
		int v = adjList[u][i];
		if (v == p) continue;
		size[u] += sizedfs(v, u);
		if (size[v] > msz) {
			fson[u] = v; msz = size[v];
		}
	}
	return size[u];
}

void builddfs(int u, int ch, int h) {
	nchain[u] = ch; id[u] = chain[ch].size();
	chain[ch].push_back(u);
	for(int i=0; i<(int)adjList[u].size(); i++) {
		int v = adjList[u][i];
		if (v == par[u]) continue;
		if (v == fson[u]) builddfs(v, ch, h+1);
		else {
			up[nchs] = u; depth[nchs] = h;
			chain[nchs].clear();
			builddfs(v, nchs++, h+1);
		}
	}
}

const int neutral = 0; //comp(x, neutral) = x
int comp(int a, int b) {
	return max(a, b);
}

class SegmentTree {
private:
	vector<int> st, lazy;
	int size;
#define parent(p) (p >> 1)
#define left(p) (p << 1)
#define right(p) ((p << 1) + 1)
	void push(int p, int l, int r) {
		//st[p] += (r - l + 1)*lazy[p];	//Caso RSQ
		st[p] += lazy[p]; 		    //Caso RMQ
		if (l != r) {
			lazy[right(p)] += lazy[p];
			lazy[left(p)] += lazy[p];
		}
		lazy[p] = 0;
	}
	void update(int p, int l, int r, int a, int b, int k) {
		push(p, l, r);
		if (a > r || b < l) return;
		else if (l >= a && r <= b) {
			lazy[p] = k; push(p, l, r);
		}
		else {
			update(left(p), l, (l + r) / 2, a, b, k);
			update(right(p), (l + r) / 2 + 1, r, a, b, k);
			st[p] = comp(st[left(p)], st[right(p)]);
		}
	}
	int query(int p, int l, int r, int a, int b) {
		push(p, l, r);
		if (a > r || b < l) return neutral;
		if (l >= a && r <= b) return st[p];
		int p1 = query(left(p), l, (l + r) / 2, a, b);
		int p2 = query(right(p), (l + r) / 2 + 1, r, a, b);
		return comp(p1, p2);
	}
public:
	SegmentTree(int _size) {
		size = _size;
		st.assign(4 * size, neutral);
		lazy.assign(4 * size, 0);
	}
	int query(int a, int b) { return query(1, 0, size - 1, a, b); }
	void update(int a, int b, int k) { update(1, 0, size - 1, a, b, k); }
};

vector<SegmentTree> hld;

void heavylightdecomposition(int _root) {
	root = _root;
	sizedfs(root, -1);
	nchs = 0; chain[0].clear();
	up[nchs] = -1; depth[nchs] = 0;
	builddfs(root, nchs++, 1);
	FOR(c, nchs) {
		hld.pb(SegmentTree(chain[c].size()));
	}
}

void update(int u, int v, int val) {
	int cu = nchain[u], cv = nchain[v];
	while(cu != cv) {
		if (depth[cu] > depth[cv]) {
			hld[cu].update(0, id[u], val);
			u = up[cu];
		}
		else {
			hld[cv].update(0, id[v], val);
			v = up[cv];
		}
		cu = nchain[u]; cv = nchain[v];
	}
	if (id[u] < id[v]) {
		hld[cu].update(id[u], id[v], val);
	}
	else {
		hld[cu].update(id[v], id[u], val);
	}
}

