/*
 * Point 2D
 */

struct point {
	int x, y;
	point() { x = y = 0.0; }
	point(int _x, int _y) : x(_x), y(_y) {}
	bool operator < (point other) const {
		if (fabs(x - other.x) > EPS) return x < other.x;
		else return y < other.y;
	}
	bool operator == (point other) const {
		return x == other.x && y == other.y;
	}
	point operator +(point other) const {
		return point(x + other.x, y + other.y);
	}
	point operator -(point other) const {
		return point(x - other.x, y - other.y);
	}
	point operator *(int k) const {
		return point(x*k, y*k);
	}
};

int inner(point p1, point p2) {
	return p1.x*p2.x + p1.y*p2.y;
}

int cross(point p1, point p2) {
	return p1.x*p2.y - p1.y*p2.x;
}

bool ccw(point p, point q, point r) {
	return cross(q-p, r-p) > 0;
}

bool collinear(point p, point q, point r) {
	return cross(p-q, r-p) == 0;
}

bool parallel(point a, point b) {
	return cross(a, b) == 0;
}

typedef vector<point> polygon;

int leftmostIndex(vector<point> &P) {
	int ans = 0;
	for(int i=1; i<(int)P.size(); i++) {
		if (P[i] < P[ans]) ans = i;
	}
	return ans;
}

point pivot(0, 0);

bool angleCmp(point a, point b) {
	if (collinear(pivot, a, b))
		return inner(pivot-a, pivot-a) < inner(pivot-b, pivot-b);
	return cross(a-pivot, b-pivot) >= 0;
}

polygon convexHull(vector<point> P) {
	int i, j, n = (int)P.size();
	if (n <= 2) return P;
	int P0 = leftmostIndex(P);
	swap(P[0], P[P0]);
	pivot = P[0];
	sort(++P.begin(), P.end(), angleCmp);
	vector<point> S;
	S.push_back(P[n-1]);
	S.push_back(P[0]);
	S.push_back(P[1]);
	for(i = 2; i < n;) {
		j = (int)S.size()-1;
		if (ccw(S[j-1], S[j], P[i])) S.push_back(P[i++]);
		else S.pop_back();
	}
	reverse(S.begin(), S.end());
	S.pop_back();
	reverse(S.begin(), S.end());
	return S;
}

