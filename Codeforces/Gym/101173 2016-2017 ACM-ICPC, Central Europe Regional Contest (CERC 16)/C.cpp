double getside() {
	if (s[0] == 'S') return 1;
	if (s[0] == 'C') return 0.5*acos(-1.0);
	point pivot = point(0.5, sqrt(3.0)/2.0);
	double ans = dist(point(0, 0), pivot);
	FOR(i, n) {
		if (s[i] == 'C') {
			circle c = circle(point(i+0.5, 0.5), 0.5);
			point tg = c.getTangentPoint(pivot);
			ans += dist(pivot, tg) + 0.5*angle(tg, c.c, point(i+0.5, 1));
			break;
		}
		if (s[i] == 'S') {
			ans += dist(pivot, point(i, 1));
			break;
		}
	}
	return ans;
}

