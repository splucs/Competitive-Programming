ll cntxy(ll cxy, vi & a, vector<bool> & used) {
	ll ans = 0;
	stack<int> ux;
	FOR(i, sz(a)) {
		if (used[i]) continue;
		if (a[i] == 0) ux.push(i);
		else {
			if (!ux.empty()) {
				used[ux.top()] = true;
				used[i] = true;
				ux.pop();
				ans += cxy;
			}
		}
	}
	return ans;
}

ll cntyx(ll cyx, vi & a, vector<bool> & used) {
	ll ans = 0;
	stack<int> uy;
	FOR(i, sz(a)) {
		if (used[i]) continue;
		if (a[i] == 1) uy.push(i);
		else {
			if (!uy.empty()) {
				used[uy.top()] = true;
				used[i] = true;
				uy.pop();
				ans += cyx;
			}
		}
	}
	return ans;
}

ll cntone(ll cx, ll cy, vi & a, vector<bool> & used) {
	ll ans = 0;
	FOR(i, sz(a)) {
		if (used[i]) continue;
		if (a[i] == 0) ans += cx;
		else ans += cy;
		used[i] = true;
	}
	return ans;
}

ll solve(int u, int v, vi & a) {
	ll cx = one.count({u, v}) ? one[{u, v}] : INFLL;
	ll cy = one.count({v, u}) ? one[{v, u}] : INFLL;
	ll cxy = rond.count({u, v}) ? rond[{u, v}] : INFLL;
	ll cyx = rond.count({v, u}) ? rond[{v, u}] : INFLL;
	cx = min(cx, cxy);
	cy = min(cy, cyx);
	cxy = min(cx + cy, cxy);
	cyx = min(cx + cy, cyx);
	//printf("cx = %d cy = %d cxy = %d cyx = %d\n", cx, cy, cxy, cyx);
	vector<bool> used(a.size());
	ll ans = 0;
	if (cx + cy <= cxy && cx + cy <= cyx) {
		ans += cntone(cx, cy, a, used);
	}
	else if (cxy <= cx + cy && cx + cy <= cyx) {
		ans += cntxy(cxy, a, used);
		ans += cntone(cx, cy, a, used);
	}
	else if (cyx <= cx + cy && cx + cy <= cxy) {
		ans += cntyx(cyx, a, used);
		ans += cntone(cx, cy, a, used);
	}
	else if (cxy <= cyx) {
		ans += cntxy(cxy, a, used);
		ans += cntyx(cyx, a, used);
		ans += cntone(cx, cy, a, used);
	}
	else {
		ans += cntyx(cyx, a, used);
		ans += cntxy(cxy, a, used);
		ans += cntone(cx, cy, a, used);
	}
	return ans;
}

