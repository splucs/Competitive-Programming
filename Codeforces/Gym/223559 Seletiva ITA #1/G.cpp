#define MAXS 8000009

const ll neutral = 0; //comp(x, neutral) = x
ll comp(ll a, ll b) {
	return a+b;
}

ll st[MAXS];
int vroot[MAXN];
int ls[MAXS], rs[MAXS];

class PersistentSegmentTree {
private:
	int size, nds, nv;
	int node(ll v) {
		ls[nds] = rs[nds] = -1;
		st[nds++] = v;
		return nds-1;
	}
	void build(int p, int l, int r, ll* A) {
		if (l == r) {
			st[p] = A ? A[l] : neutral;
		}
		else {
			ls[p] = node(neutral);
			rs[p] = node(neutral);
			int m = (l + r) / 2;
			build(ls[p], l, m, A);
			build(rs[p], m+1, r, A);
			st[p] = comp(st[ls[p]], st[rs[p]]);
		}
	}
	void update(int prv, int p, int l, int r, int i, ll k) {
		if (i > r || i < l || l > r) return;
		int m = (l + r) / 2;
		if (l == r) st[p] += k; //adiciona
		//if (l == r) st[p] = k; //substitui
		else if (i <= m) {
			rs[p] = rs[prv];
			ls[p] = node(st[ls[prv]]);
			update(ls[prv], ls[p], l, m, i, k);
			st[p] = comp(st[ls[p]], st[rs[p]]);
		}
		else {
			ls[p] = ls[prv];
			rs[p] = node(st[rs[prv]]);
			update(rs[prv], rs[p], m+1, r, i, k);
			st[p] = comp(st[ls[p]], st[rs[p]]);
		}
	}
	ll query(int p, int l, int r, int a, int b) {
		if (a > r || b < l || l > r) return neutral;
		if (l >= a && r <= b) return st[p];
		ll p1 = query(ls[p], l, (l + r) / 2, a, b);
		ll p2 = query(rs[p], (l + r) / 2 + 1, r, a, b);
		return comp(p1, p2);
	}
public:
	PersistentSegmentTree() { size = nds = nv = 0; }
	PersistentSegmentTree(ll* begin, ll* end) {
		nds = nv = 0; size = (int)(end-begin);
		vroot[nv++] = node(neutral);
		build(vroot[0], 0, size-1, begin);
	}
	PersistentSegmentTree(int _size) {
		nds = nv = 0; size = _size;
		vroot[nv++] = node(neutral);
		build(vroot[0], 0, size-1, NULL);
	}
	ll query(int v, int a, int b) { return query(vroot[v], 0, size-1, a, b); }
	int update(int v, int i, ll k) {
		vroot[nv++] = node(st[vroot[v]]);
		update(vroot[v], vroot[nv-1], 0, size-1, i, k);
		return nv-1;
	}
	int nver() { return nv; }
};

