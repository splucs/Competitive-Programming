int ned, prv[MAXN], first[MAXN];
int cap[MAXM], cpy[MAXM], to[MAXM], nxt[MAXM], dist[MAXN];

void init() {
   memset(first, -1, sizeof first);
   ned = 0;
}

void add(int u, int v, int f) {
    to[ned] = v, cap[ned] = f, cpy[ned] = f;
    nxt[ned] = first[u];
    first[u] = ned++;
    
    to[ned] = u, cap[ned] = 0, cpy[ned] = 0;
    nxt[ned] = first[v];
    first[v] = ned++;
}

int augment(int v, int minEdge, int s) {
	int e = prv[v];
	if (e == -1) return minEdge;
	int f = augment(to[e^1], min(minEdge, cap[e]), s);
	cap[e] -= f;
	cap[e^1] += f;
	return f;
}

bool bfs(int s, int t) {
	int u, v;
	memset(&dist, -1, sizeof dist);
	dist[s] = 0;
	queue<int> q; q.push(s);
	memset(&prv, -1, sizeof prv);
	while (!q.empty()) {
		u = q.front(); q.pop();
		if (u == t) break;
		for(int e = first[u]; e!=-1; e = nxt[e]) {
			v = to[e];
			if (dist[v] < 0 && cap[e] > 0) {
				dist[v] = dist[u] + 1;
				q.push(v);
				prv[v] = e;
			}
		}
	}
	return dist[t] >= 0;
}

int edmondskarp(int s, int t) {
	int result = 0;
	while (bfs(s, t)) {
		result += augment(t, INF, s);
	}
	return result;
}

int n, k, s, t;
char adj[MAXN][MAXN];
int g[MAXN][MAXN];
int in[MAXN], out[MAXN];

void buildg() {
	FOR1(u, n) {
		for(int e = first[u]; e!=-1; e = nxt[e]) {
			int v = to[e];
			if (v == t) continue;
			while (cap[e] < cpy[e]) {
				cap[e]++;
				g[u][v]++;
				//printf("%d->%d\n", u, v);
				in[v]++;
				out[u]++;
			}
		}
	}
}

bool mine[MAXN];
int a[MAXN], b[MAXN];

int main() {
	while(scanf("%d %d", &n, &k) != EOF) {
		init();
		s = 0;
		t = n+1;
		mset(mine, false);
		FOR(i, k) {
			scanf("%d", &a[i]);
			mine[a[i]] = true;
			add(s, a[i], 1);
		}
		FOR(i, k) {
			scanf("%d", &b[i]);
			add(b[i], t, 1);
		}
		FOR(i, n) {
			scanf(" %s", adj[i]);
			FOR(j, n) {
				if (adj[i][j] == '1') add(i+1, j+1, n);
			}
		}
		bool ok = true;
		if (edmondskarp(s, t) != k) {
			ok = false;
		}
		
		vector<ii> ans;
		buildg();
		while(true) {
			bool found = false;
			FOR1(u, n) {
				if (!mine[u]) continue;
				FOR1(v, n) {
					if (g[u][v] && !mine[v]) {
						swap(mine[u], mine[v]);
						g[u][v]--;
						found = true;
						ans.pb({u, v});
						break;
					}
				}
			}
			if (!found) break;
		}
		FOR(i, k) {
			if (!mine[b[i]]) ok = false;
		}

		if (!ok) {
			printf("NO\n");
		}
		else {
			printf("YES\n");
			printf("%u\n", ans.size());
			for(ii pp : ans) {
				printf("%d %d\n", pp.fi, pp.se);
			}
		}
	}
	return 0;
}