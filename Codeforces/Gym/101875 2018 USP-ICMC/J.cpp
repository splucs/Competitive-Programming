inline void refresh(int u, int i) {
	if (i < 0 || i >= sz(cnt[u])) return;
	cnt[u][i].fi += cnt[u][i].se;
	if (i+1 < sz(cnt[u])) cnt[u][i+1].se += cnt[u][i].se;
	cnt[u][i].se = 0;
	ans = max(ans, cnt[u][i].fi);
}

void checkpos(int u, int i) {
	assert(i >= 0 && i < sz(cnt[u]));
}

