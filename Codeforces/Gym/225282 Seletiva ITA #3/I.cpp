inline bool isOp(char c) {
	return c=='+' || c=='-' || c=='*' || c=='/' || c=='^';
}

inline bool isCarac(char c) {
	return (c>='a' && c<='z') || (c>='A' && c<='Z') || (c>='0' && c<='9');
}

void paren2polish(const char* paren, vector< pair<int, char> > & polish, int & numpar) {
	map<char, int> prec;
	prec['('] = 0;
	prec['+'] = prec['-'] = 1;
	prec['*'] = prec['/'] = 2;
	prec['^'] = 3;
	int len = 0;
	stack<char> op;
	numpar = 0;
	polish.clear();
	for (int i = 0; paren[i]; i++) {
		if (isOp(paren[i])) {
			while (!op.empty() && prec[op.top()] >= prec[paren[i]]) {
				polish.pb({1, op.top()}); op.pop();
			}
			op.push(paren[i]);
		}
		else if (paren[i]=='(') {
			op.push('(');
			numpar++;
		}
		else if (paren[i]==')') {
			for (; op.top()!='('; op.pop())
				polish.pb({1, op.top()});
			op.pop();
		}
		else if (isCarac(paren[i])) {
			char atom = paren[i] - '0';
			if (i > 0 && isCarac(paren[i-1])) {
				atom += 10*polish.back().se;
				polish.pop_back();
			}
			polish.pb({0, atom});
		}
	}
	for(; !op.empty(); op.pop())
		polish.pb({1, op.top()});
}

