vector<ii> decompose(ll N) {
	vector<ii> ans;
	for(ll i = 2; i*i <= N; i++) {
		if (N%i == 0) {
			int cnt = 0;
			while(N%i == 0) {
				N /= i;
				cnt++;
			}
			ans.pb({i, cnt});
		}
	}
	if (N > 1) ans.pb({N, 1});
	/*printf("K = ");
	for(ii pp : ans) {
		printf("(%lld^%d)", pp.fi, pp.se);
	}
	printf("\n");*/
	return ans;
}

ll K, R;
int N;
