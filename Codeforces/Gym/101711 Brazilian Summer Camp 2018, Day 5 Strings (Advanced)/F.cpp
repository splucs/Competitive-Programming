//Impressão
void print(bignum & a) {
	printf("%d", a.empty() ? 0 : a.back());
	for (int i=(int)a.size()-2; i>=0; --i) {
		printf("%09d", a[i]);
	}
}

//Remover Leading zeros
void fix(bignum & a) {
	while (a.size() > 1u && a.back() == 0) {
		a.pop_back();
	}
}

//Construtor a partir de um inteiro
void int2bignum(int n, bignum & a) {
	a.clear();
	if (n == 0) a.push_back(0);
	while(n > 0) {
		a.push_back(n%base);
		n /= base;
	}
}

//Soma a+b=c
void sum(bignum & a, bignum & b, bignum & c) {
	int carry = 0, n = max(a.size(), b.size());
	c.resize(n);
	for (int i=0, ai, bi; i<n; i++) {
		ai = i < (int)a.size() ? a[i] : 0;
		bi = i < (int)b.size() ? b[i] : 0;
		c[i] = carry + ai + bi;
		carry = c[i] / base;
		c[i] %= base;
	}
	if (carry > 0) c.push_back(carry);
	fix(c);
}

char alpha[MAXN], sz;

struct node {
	node *fail, *next[ALFA];
	vector<char> adj;
	vector<int> pats;
	int nid;
	node(vector<node*> & st) {
		fail = NULL;
		nid = st.size();
		memset(&next, 0, sizeof next);
		st.push_back(this);
	}
};

class AhoCorasick {
	//Automaton build
private:
	node *trie;
	map<int, int> sizes;
	vector<node*> st;
	node *suffix(node *x, char c) {
		while (x != trie && x->next[c] == 0) x = x->fail;
		return (x->next[c] ? x->next[c] : trie);
	}
public:
	AhoCorasick() { clear(); }
	void clear() {
		for(int i=0; i<(int)st.size(); i++) delete st[i];
		st.clear();
		sizes.clear();
		trie = new node(st);
	}
	void setfails() {
		queue<node*> q;
		node *x, *y;
		for (int i = 0; i < ALFA; i++) {
			x = trie->next[i];
			if (x != NULL && x != trie) {
				x->fail = trie;
				q.push(x);
			}
		}
		while (!q.empty()) {
			x = q.front(); q.pop();
			for (int i = 0; i < (int)x->adj.size(); i++) {
				y = x->next[x->adj[i]];
				y->fail = suffix(x->fail, x->adj[i]);
				y->pats.insert(y->pats.end(),
					y->fail->pats.begin(), y->fail->pats.end());
				q.push(y);
			}
		}
	}
	void insert(const char* s, int id) {
		int len = strlen(s);
		sizes[id] = len;
		node *x = trie, *y;
		for (int i = 0; i < len; i++) {
			y = x->next[s[i]];
			if (y == NULL || y == trie) {
				x->next[s[i]] = new node(st);
				x->adj.push_back(s[i]);
			}
			x = x->next[s[i]];
		}
		x->pats.push_back(id);
	}
	vector<ii> match(const char *s) {
		node* x = trie;
		int len = strlen(s), id;
		vector<ii> ans;
		for (int i = 0; i < len; i++) {
			x = suffix(x, s[i]);
			for (int j = 0; j < (int)x->pats.size(); j++) {
				id = x->pats[j];
				ans.push_back(ii(id, i - sizes[id]));
			}
		}
		return ans;
	}

	//Dynamic Programming (size left, appeared, node id)
private:
	bignum dp[MAXN][MAXM];
	bignum DP(const int s, node* x) {
		if (x == NULL) {
			bignum aux;
			int2bignum(0, aux);
			return aux;
		}
		if (!dp[s][x->nid].empty()) return dp[s][x->nid];
		int2bignum(0, dp[s][x->nid]);
		if (!x->pats.empty()) {
			return dp[s][x->nid];
		}
		if (s == 0) {
			int2bignum(1, dp[s][x->nid]);
			return dp[s][x->nid];
		}
		FOR(i, sz) {
			char c = alpha[i];
			bignum nxt = DP(s - 1, suffix(x, c));
			sum(dp[s][x->nid], nxt, dp[s][x->nid]);
		}
		return dp[s][x->nid];
	}
public:
	bignum DP(int m) {
		return DP(m, trie);
	}
	void initDP() {
		for (int i = 0; i < MAXN; i++)
			for (int j = 0; j < MAXM; j++)
				dp[i][j].clear();
	}
};

